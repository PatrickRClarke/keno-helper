// Creates bit strings.
string bs_er(vector<game> a, int b)
{
    string z;
    bool x = false;

    for(game i : a)
    {   

        vector<int> y = i.get_num_s();

        for(int j : y)
        {
            if(j == b)
                x = true;
        }

            if(z.length() % 5 == 0)
                z.push_back(' ');

            if(x == true)
                z.push_back('1');
            else
                z.push_back('0');

        x = false;

    }

    z.erase(0,1);
    return z;

}

